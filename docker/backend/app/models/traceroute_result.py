"""TracerouteResult: one row per hop of a traceroute run.

The Traceroute monitor (Step 13) never runs continuously — it triggers
on-incident. A single run produces many hops, so rows share a `run_id`
(the grouping key for "show me this traceroute") and optionally an
`incident_id` (nullable: a traceroute could in principle be triggered
manually later, e.g. from the dashboard, without an incident behind it).
"""

from __future__ import annotations

import uuid
from datetime import datetime

from sqlalchemy import Boolean, DateTime, Float, ForeignKey, Index, Integer, String
from sqlalchemy.orm import Mapped, mapped_column, relationship

from app.database.base import Base
from app.database.mixins import IntPKMixin


class TracerouteResult(Base, IntPKMixin):
    __tablename__ = "traceroute_results"
    __table_args__ = (Index("ix_traceroute_results_run_id_hop", "run_id", "hop_number"),)

    # Groups all hops of a single traceroute execution. UUID (not the
    # integer PK) because this value is generated by the monitor at
    # trigger-time, before any row exists — a plain string key avoids a
    # chicken-and-egg dependency on the DB assigning the first row's id.
    run_id: Mapped[str] = mapped_column(String(36), nullable=False, default=lambda: str(uuid.uuid4()))

    incident_id: Mapped[int | None] = mapped_column(
        ForeignKey("incidents.id", ondelete="CASCADE"), nullable=True
    )

    target: Mapped[str] = mapped_column(String(255), nullable=False)
    timestamp: Mapped[datetime] = mapped_column(DateTime(timezone=True), nullable=False)

    hop_number: Mapped[int] = mapped_column(Integer, nullable=False)
    hop_ip: Mapped[str | None] = mapped_column(String(45), nullable=True)  # IPv4/IPv6
    hop_hostname: Mapped[str | None] = mapped_column(String(255), nullable=True)
    latency_ms: Mapped[float | None] = mapped_column(Float, nullable=True)
    is_timeout: Mapped[bool] = mapped_column(Boolean, nullable=False, default=False)

    incident: Mapped["Incident | None"] = relationship(back_populates="traceroute_results")  # noqa: F821

    def __repr__(self) -> str:  # pragma: no cover
        return (
            f"<TracerouteResult run_id={self.run_id} hop={self.hop_number} "
            f"hop_ip={self.hop_ip!r} latency_ms={self.latency_ms}>"
        )
